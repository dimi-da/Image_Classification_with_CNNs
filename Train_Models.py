#load dataset (images) from files; data are expected to be stored in two separate folders, each containing all images belonging to the same class
#input: file-path to folders containing the data
#output: list of images
#        list of class_names
def create_dataset(path):
	img_data_array=[]
	class_name=[]
	
	for directory in [name for name in os.listdir(path) if os.path.isdir(os.path.join(path, name))]:
		for file in os.listdir(os.path.join(path, directory)):
			if not os.path.isdir(file) and not "check" in file: #exclude folders and data automatically generated by JupyterLab
				image_path = os.path.join(path, directory, file)
				image = mpimg.imread(image_path)
				image = np.array(image)
				image = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY) #reduce image to one channel (gray)
				image = image.astype('float32')
				img_data_array.append(image)
				class_name.append(directory)
	return img_data_array, class_name 


#calculate the error estimate
#input: list of true values
#       list of predicted values
#output: cacluated error
def error_estimate(y_true, y_pred): 
	y_pred = tf.keras.backend.clip(y_pred, 0, 1) #make sure all values are between 0 and 1
	y_pred = tf.keras.backend.round(y_pred) #round values in the prediction
	pgt = tf.greater(y_pred, y_true) #prediction value greater true value
	pgt = tf.keras.backend.cast(pgt, 'float32') #cast bool to float
	tgp = tf.greater(y_true, y_pred) #true value greater prediction value
	tgp = tf.keras.backend.cast(tgp, 'float32') #cast bool to float
	
	s = tf.reduce_sum(pgt, axis=-1) + tf.reduce_sum(tgp, axis=-1) #count how often prediction was greater true plus how often true was greater prediction (i.e., count wrong predictions)
	
	#return s / K.int_shape(y_pred)[-1] #divide by the number of elements in the tensor's last dimension
	return s / tf.cast(y_pred.shape[-1], tf.float32)

#create a keras neural network model
#input: number of CNNs (t)
#       number of filters in the CNNs
#       number of convolutional / hidden layers (Ln) 
#       level of the network (l)
#       and the neurons in the hidden layers 
#output: keras model
def create_model(t, filter_count, Ln, l, neurons): 
	IMG_HEIGHT = 32
	IMG_WIDTH = 32
	
	#loss_function to be used for training the neural network is set to Mean Squared Error (mse)
	loss_function = 'mse'
	
	#empty list for layers to be stored
	flat = []
	layers = [[]]
	fcff_layers = []
	inputs = []
	
	#convolutional neural network(s)
	for k in range(t): 
		layers.append([]) #append a new empty array to layers (for the second dimension)
		#input-layer: input_shpe = 32x32x1 (32 x 32 pixel with one gray scale value per pixel)
		layers[k].append(tf.keras.layers.Input(shape=(IMG_HEIGHT, IMG_WIDTH, 1))) 
		inputs.append(layers[k][-1]) #safe the input-layers separatly for the model creation
		
		#construct convolutional layers
		#number of layers: l * L_n
		for i in range(1, l+1):
			#calculate filter_size (2^1 in the frist L_n layers, 2^2 in the second L_n layers, ... , 2^l in the l-th L_n layers)
			kernel_size = 2 ** i 
			for j in range(Ln):
				#add a one-sided zero-padding to the input of the next convolutional layer, such that the size of the feature-map stays the same (i.e., adding padding of size kernel_size-1)
				layers[k].append(tf.keras.layers.ZeroPadding2D(((0, kernel_size-1), (0, kernel_size-1)))(layers[k][-1])) 
				#adding convutional layers, with k^(1) many filters, kernel size (from 2^1 ... 2^l), stride = 1, activation-function 'relu' and using a bias.
				layers[k].append(tf.keras.layers.Conv2D(filters = filter_count, kernel_size = kernel_size, strides = 1, padding = 'valid', activation = 'relu', use_bias=True, kernel_initializer="glorot_uniform", bias_initializer="zeros", name='ConvLayer_'+str(k)+'_'+str(i)+'-'+str(j)+'_'+str(kernel_size))(layers[k][-1])) 
		
		#output weigth as a convolutional layer with filter-size 1, i.e., the filter is a single number which is multiplied with each element of a channel's feature-map, and the channels are added element-wise
		layers[k].append(tf.keras.layers.Conv2D(filters = 1, kernel_size = 1, strides = 1, padding = 'valid', activation = 'linear', use_bias=False, name='LinearLayer_multiplcation_'+str(k))(layers[k][-1])) 
		
		#max-pooling of the linear layer's output. The pool_size equals to the image dimensions minus filter-size of the last convutional layer plus one
		layers[k].append(tf.keras.layers.MaxPool2D(pool_size = IMG_HEIGHT - kernel_size + 1, strides = None, name="MaxPool_"+str(k))(layers[k][-1])) # pool_size = filter_size of last convutional layer 
		
		#flatten the network to pass it to the concatenation layer and afterwards to the fully connected neural network
		flat.append(tf.keras.layers.Flatten()(layers[k][len(layers[k])-1])) 
	
	#concatenate CNN-outputs if there is more than one CNN 
	if(len(flat) > 1):
		fcff_layers.append(tf.keras.layers.concatenate(flat)) 
	else:
		fcff_layers.append(flat[0])
	
	#fully connected multilayer feedforward neural network
	
	#create L_n many layers with k^(2) many neurons per layer
	for j in range(Ln):
		fcff_layers.append(tf.keras.layers.Dense(units = neurons, use_bias = True, kernel_initializer='random_normal', bias_initializer='zeros', activation = 'relu')(fcff_layers[-1])) 
	
	#output layer, with only one neuron producing a prediction of the image's class
	outputs = tf.keras.layers.Dense(units = 1, use_bias = True, activation = 'linear')(fcff_layers[-1]) 
	
	#create a keras model from the layers created above
	model = tf.keras.Model(inputs=inputs, outputs=outputs)
	#use the Adam-optimizer
	opt = tf.keras.optimizers.Adam()
	model.compile(optimizer=opt, loss=loss_function, metrics=[error_estimate]) 
	
	return model


