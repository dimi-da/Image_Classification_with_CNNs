#load images and their classes from the specified path. 
images, class_name = create_dataset(r'3obj-images') 

#convert data to match input format of the neural network, store the images in variable data_X
data_X = np.array(images, np.float32) 
data_X = np.expand_dims(data_X, axis=3) #make each gray-scale value an array, to meet input format expectation of keras


#convert class names into numbers and store the list of classes in variable data_y
class_dict = {k: v for v, k in enumerate(np.unique(class_name))} 
data_y = [class_dict[class_name[i]] for i in range(len(class_name))]
data_y = np.array(data_y, np.float32) #convert labels to np-array of type float32, to meet input format expectation of keras


#permute the input data
perm = permutation(len(data_X)) 
data_X = data_X[perm]
data_y = data_y[perm]


#set the validation split, use 80% of data for training and 20% for validation
validation_split = 0.2 


#set list of value to select from for every hyperparameter
t = [1, 2] # t \in {1,2} 
k1 = [2, 4, 8] # k^(1) \in {2, 4, 8}
Ln = [1, 2, 3] #L_n \in {1, 2, 3}
l = [2, 3, 4] # l \in {2, 3, 4}
k2 = [5, 10] # k^2 \in {5, 10}


x = [] #for multi-headed networks the input data must be provided as list (of images)

#iterate over all possible hyperparameter combinations 
for ti in t: 
	x.append(data_X) #repeat data t times in a list as input for a model with t heads
	for k1i in k1:
		for Lni in Ln:
			for li in l:
				for k2i in k2:
					#create a neural network model for the hyperparameters
					model = create_model(t = ti, filter_count = k1i, Ln = Lni, l = li, neurons = k2i) 
					#check if the number of trainable parameters of the model is larger than the number of input samples. If its larger it is skipped
					if(trainable_params_count(model) > len(data_X)*(1.0-validation_split)): 
						print("too many trainable parameters ("+str(trainable_params_count(model))+") for t="+str(ti)+", k1="+str(k1i)+", Ln="+str(Lni)+", l="+str(li)+", k2="+str(k2i)+"! skipping")
					#if the number of trainable parameters it not too large the model is trained
					else:
						print("Running parameter-set t="+str(ti)+", k1="+str(k1i)+", Ln="+str(Lni)+", l="+str(li)+", k2="+str(k2i)+" ("+str(trainable_params_count(model))+" trainable parameters)")
						#train the model
						history = model.fit(x = x, y = data_y, epochs = 2000, validation_split=validation_split) 
						#save trained parameters of the model
						model.save_weights("models/"+str(ti)+"_"+str(k1i)+"_"+str(Lni)+"_"+str(li)+"_"+str(k2i)+".weights.h5")
						#saving validation results
						np.save("history/"+str(ti)+"_"+str(k1i)+"_"+str(Lni)+"_"+str(li)+"_"+str(k2i), history.history)
